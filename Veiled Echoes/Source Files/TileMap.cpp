TileMap::TileMap()
{
	dirt = TextureManager::LoadTexture("Assets/dirt.png");
	grass = TextureManager::LoadTexture("Assets/grass.png");
	water = TextureManager::LoadTexture("Assets/water.png");

	LoadTileMap(lvl1);
	
	srcRect.x = srcRect.y = 0;
	srcRect.w = destRect.w = 32;
	srcRect.h = destRect.h = 32;

	destRect.x = destRect.y = 0;
}

TileMap::~TileMap()
{

}

void TileMap::LoadTileMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			tileMap[row][column] = arr[row][column];

		}

	}

}

void TileMap::DrawTileMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = tileMap[row][column];

			destRect.x = column * 32;
			destRect.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(water, srcRect, destRect);
				break;
			case 1:
				TextureManager::Draw(grass, srcRect, destRect);
				break;
			case 2:
				TextureManager::Draw(dirt, srcRect, destRect);
				break;

			default:
				break;
			}

		}

	}

}